num1 = int(input("Enter a number"))
operation = input("Enter an operation")
num2 = int(input("Enter another number"))

if operation == "+":
    print(str(num1 + num2))
    
elif operation == "-":
    print(str(num1 - num2))

elif operation == "*":
    print(str(num1 * num2))

elif operation == "/":
    print(str(num1 / num2))